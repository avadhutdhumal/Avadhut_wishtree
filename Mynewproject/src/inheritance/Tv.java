package inheritance;

public class Tv {

	private String tvbrand;
	private int tvprice;
	private String color;
	private Home_Theater h;
	
	public String getTvbrand() 
	{
		return tvbrand;
	}
	public void setTvbrand(String tvbrand) 
	{
		this.tvbrand = tvbrand;
	}
	public int getTvprice() 
	{
		return tvprice;
	}
	public void setTvprice(int tvprice) 
	{
		this.tvprice = tvprice;
	}
	public String getColor() 
	{
		return color;
	}
	public void setColor(String color)
	{
		this.color = color;
	}
	public Home_Theater getHome_Theater() 
	{
		return h;
	}
	public void setHome_Theater(Home_Theater h) 
	{
		this.h = h;
	}
	
	
	

}

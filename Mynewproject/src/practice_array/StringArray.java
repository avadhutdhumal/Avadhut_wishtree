package practice_array;
public class StringArray {

	public static void main(String[] args)
	{
	     String s[] = {"as","asd","gffd","fgf"};
	     System.out.println(s.length);
	     for(String str : s)
	     {
	    	 System.out.print(str + " =>");
	    	 System.out.println("Length of String :" + str.length());
	   	 
	     } 
	     
	}

}

package Written_Test2;

public class Ques26 {

	public static void main(String[] args) {
		System.out.println("For Loop");

		for (int i = 1; i < 6; i++) {
			System.out.print(i + " ");
		}

		System.out.println("\n\nDo While");
		int i = 1;

		do {
			System.out.print(i++ + "  ");
		} while(i<6);

	}

}
